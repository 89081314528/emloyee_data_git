package com.company;

public class Employee {
    String surname;
    String name;
    String middleName;
    int passportSeries;
    int passportNamber;
    int salary;
    public Employee (String surname, String name, String middleName, int passportSeries, int passportNamber, int salary) {
        this.surname = surname;
        this.name = name;
        this.middleName = middleName;
        this.passportSeries = passportSeries;
        this.passportNamber = passportNamber;
        this.salary = salary;
    }

}
